  /**
   * Checks if a Chinese name has a combo last name.
   * 
   * @param name
   *          a Chinese name
   * @return true if the name has a combo last name, otherwise false
   */
  public static boolean hasComboLastName(String name) {
    if (name.length() > 2) {
      try {
        ComboLastName.valueOf(name.substring(0, 2));
        return true;
      } catch (Exception e) {
        return false;
      }
    }
    return false;
  }

